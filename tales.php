	I have to smack down 3 underage Ettins who were attacking a beer
	elemental, but I finally get the beer elemental to tell me where the
	porn elementals are after a few rounds. I find em, get my lube, and
	hurry back to the succubus.
	<br>
	<br>Invisible Hands VII is still up, but I have 2 new
	problems. Orcus and Yeenoghu are there and my Succubus is
	complaining she is thristy. Fuck. Lucky for me Orcus and Yeenoghu
	are starting to fight each other over who gets the succubus so I have
	some time. Knowing my Inivisible Hands isn't going to last the whole
	time, I hit the Succubus with Slime Finger XII and run off to find
	some water.
	<br>
