	<h1 align=center>Developer Diary</h1>
	
	<p>
	<i>
	Development on the long awaited Progress Quest OEM Service Release 2 (OSR2) continues
	apace. The new version is informally named "Progress Quest Realms" as it 
	will include support for multiple realms, as well as a few
	new beasts, items, and characters. To keep you abreast of whats afoot, we'll be introducing
	previews of the different features in the developer diaries below. 
	Please be sure to check back every couple
	of days to see what turns up.
	</i>
	
