	<p class=d>April 8, 2002
	<p class=h>New Beta Version: RELEASE CANDIDATE 1
	<p class=t>
	You may now, and for a limited time only, 
	<a href=dl.php?s=d#rc>download the release candidate for the new version</a>.  
	
	<p class=d>April 5, 2002
	<p class=h>New Weapon Attribute: STABBITY
	<p class=t>
	The rumors surrounding this weapon attribute are, we may now reveal,
	well founded. The entire stabscape of stabbity weapons are currently undergoing rigorous
	testing in our quality labs. 
	
	<p class=d>March 29, 2002
	<p class=h>New Realm: SPOLTOG
	<p class=t>
	Spoltog is a realm of dappled sunlight on golden fields where bugbears graze. 
	In this magical land, Silkies frolic on white sand beaches, and the buzz of grid bugs hints that 
	killing is in the air. 
	
	<p class=d>March 22, 2002
	<p class=h>New Monster: MULTICELL
	<p class=t>
	The Multicell is a complex biological structure consisting of
	many different cells performing highly specialized functions,
	working together within a cohesive system.
	This vituosity makes the Multicell extremely deadly at close 
	quarters. The dendrites of the Multicell are much prized by
	apothecaries and are known to counter lockjaw.
	
	<p class=d>March 21, 2002
	<p class=h>New Race: LAND SQUID
	<p class=t>
	Posessed of superhuman strength, the Land Squid, a giant, lunged cephalopod,
	makes for a fearsome fighter. But its real strength rests in its 
	brace of dexterous iron-shod tentacles, which make the Land Squid an
	ideal Fighter/Organist; it is also exceedingly well suited to the
	Robot and Mu-Fu Monk professions, having a natural talent for "unarmed" combat.
	
	<p class=d>March 17, 2002
	<p class=h>New Monster: STUN BEAR
	<p class=t>
	The Stun Bear is a member of the Grizzly family which has been
	genetically altered and mechanically enhanced by the Vorlak. While
	sharing the bloodthirty might of its more common cousin, the Stun Bear
	has the additional ability to clap his mighty forepaws together,
	delivering a shock wave capable of immobilizing all (2d6) nearby
	enemies for a short (1d8) period. The Stun Bear, when thus at leisure,
	typically avails himself of large mouthfuls of his incapacitaed
	victims, and is thus a rather deadly opponent.
	
	
