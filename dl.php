<h2>Other Platforms</h2>

On all other platforms I humbly suggest just playing <a href="play/"><b>the web version</b></a> in your browser
of choice.

<p>There are a few other options however:

<p>

<b>Ubuntu:</b>

For <a href=http://ubuntu.com/>Ubuntu</a> users, <a href=ubuntu/>this
comprehensive guide</a> explains each step of the process of
installation on Ubuntu. It uses <a
href=http://packages.ubuntu.com/hardy/games/pq>this Ubuntu package</a>
available for Hardy Heron, that seems to work on Feisty Fawn as well.
Perhaps it will also work on other Debian-based distros.

<p>

<b>Linux &amp; other Unixen:</b> PQ should also run on other Linux distributions and maybe even other Unix distributions, via <a href=http://www.winehq.com/>Wine</a>. Download the game using <a href=#latest>the link below</a> and give it a whirl.

<p>
<b>macOS:</b>
Maybe PQ works using <a href=http://www.apple.com/macosx/features/bootcamp.html>Boot Camp</a>? 
There's been a report that it will work under <a href="http://darwine.sourceforge.net/">Darwine</a> on OS X 10.4 Tiger as well.


